+ (UILabel *)differentColorFrame:(CGRect) frame Label:(NSString *)titleFromSever title:(NSString *)title beforeColor:(UIColor *)beforeColor laterColor:(UIColor *)laterColor{
    UILabel *titleLabel = [[UILabel alloc]initWithFrame:frame];
    NSMutableString *str = [NSMutableString stringWithFormat:@"%@\n%@",titleFromSever,title];
    NSMutableAttributedString *lastStr = [[NSMutableAttributedString alloc]initWithString:str];
    NSRange oneRangel = [[lastStr string] rangeOfString:[str substringToIndex:titleFromSever.length]];
    NSRange twoRangel = [[lastStr string] rangeOfString:[str substringFromIndex:titleFromSever.length]];
    [lastStr addAttribute:NSForegroundColorAttributeName value:beforeColor range:oneRangel];
    [lastStr addAttribute:NSForegroundColorAttributeName value:laterColor range:twoRangel];
    [lastStr addAttribute:NSFontAttributeName value:[UIFont systemFontOfSize:14] range:oneRangel];
    [lastStr addAttribute:NSFontAttributeName value:[UIFont systemFontOfSize:12] range:twoRangel];
    titleLabel.numberOfLines = 2;
    titleLabel.textAlignment = NSTextAlignmentLeft;
    [titleLabel setAttributedText:lastStr];
    return titleLabel;
}

